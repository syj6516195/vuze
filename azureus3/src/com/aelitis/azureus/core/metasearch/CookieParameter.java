package com.aelitis.azureus.core.metasearch;

public class CookieParameter {
	
	String name;
	String value;
	
	public CookieParameter(String name, String value) {
		super();
		this.name = name;
		this.value = value;
	}
	
	public String getName() {
		return name;
	}
	
	public String getValue() {
		return value;
	}
	
	

}
